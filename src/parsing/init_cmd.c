#include <minishell.h>

t_cmd	*init_cmd(void)
{
	t_cmd	*cmd;

	cmd = malloc(sizeof(t_cmd));
	if (!cmd)
		return (NULL);
	cmd->args = NULL;
	cmd->infile = NULL;
	cmd->outfile = NULL;
	cmd->append = 0;
	cmd->pipe[0] = -1;
	cmd->pipe[1] = -1;
	cmd->pid = 0;
	cmd->heredoc_fd = -1;
	cmd->next = NULL;
	return (cmd);
}
