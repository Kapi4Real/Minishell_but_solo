#include <minishell.h>

void	free_cmd(t_cmd *cmd)
{
	int		i;
	t_cmd	*next;

	while (cmd)
	{
		next = cmd->next;
		if (cmd->args)
		{
			i = 0;
			while (cmd->args[i])
			{
				free(cmd->args[i]);
				i++;
			}
			free(cmd->args);
		}
		if (cmd->heredoc_fd >= 0)
			close(cmd->heredoc_fd);
		free(cmd->infile);
		free(cmd->outfile);
		free(cmd);
		cmd = next;
	}
}
